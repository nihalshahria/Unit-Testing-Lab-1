package task1;

public class Product {
    public int id;
    public String name;
    public String group;
    public String subgroup;
    public float price;
    public Product(int id, String name, String group, String subgroup, float price) {
        this.id = id;
        this.name = name;
        this.group = group;
        this.subgroup = subgroup;
        this.price = price;
    }
}
